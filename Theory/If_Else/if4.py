print(ord("u"), ord("l")) # показывает код символа из символа в соответствии с таблицей кодировок UTF-8
print(chr(117), chr(108)) # показывает символ из кода в соответствии с таблицей кодировок UTF-8
# В таблице кодировки большие и маленькие буквы являются различными символами с разными кодами (из разных диапазонов). 
# Поэтому для корректного сравнения строки должны быть в одном регистре.