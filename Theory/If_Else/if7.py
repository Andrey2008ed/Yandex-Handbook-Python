a = 1
b = 15
c = 141
# максимальное и минимально число
print(max(a, b, c), min(a, b, c))

line_1 = "я"
line_2 = "ф"
line_3 = "Я"
# минимальная лексикографически строка и максимальная (А<AA<AAA<Б<ББ<...<ЯЯЯ)
print(min(line_1, line_2, line_3), max(line_1, line_2, line_3))

# количество символов в числе 2 в степени 2022 и в слове "Количество"
print(len(str(2 ** 2022)))
print(len(str("Количество")))
