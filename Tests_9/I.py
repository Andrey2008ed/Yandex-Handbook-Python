"""
Файловая чистка

Python в первую очередь скриптовый язык. Такие языки часто используются для создания консольных утилит.

Напишите простую утилиту, которая очищает заданный файл от:

повторяющихся пробелов;
повторяющихся символов перевода строки;
табуляций,
излишних пробелов в начале и конце строк.
Формат ввода
Пользователь вводит два имени файлов.
Входной файл содержит неформатированный текст произвольной длины.

Формат вывода
Во второй файл выведите очищенный текст.

Ввод:
# Пользовательский ввод:
first.txt
second.txt

# Содержимое файла first.txt
    очень 		 плохо форматированный       текст


ну		ну	
прямо

очень-очень

	
Вывод:
# Содержимое файла second.txt
очень плохо форматированный текст
нуну
прямо
очень-очень

"""

def clean_text(input_file, output_file):
    # читаем файл построчно
    with open(input_file, 'r', encoding='utf-8') as f_in:
        lines = f_in.readlines()

    # очищаем каждую строку
    cleaned_lines = []
    for line in lines:
        # удаляем лишние пробелы в начале и конце строки
        line = line.strip()
        # удаляем повторяющиеся пробелы и все табуляции
        line = ' '.join(line.replace("\t", "").split())
        if line:
            # добавляем перевод строки, если строка не пустая
            cleaned_lines.append(line + '\n')
        else:
            # иначе добавляем пустую строку
            cleaned_lines.append(line)

    # записываем очищенный текст в файл
    with open(output_file, 'w', encoding='utf-8') as f_out:
        f_out.writelines(cleaned_lines)

# основная программа
def main():
    # спрашиваем у пользователя имена файлов
    input_file = input()
    output_file = input()

    # вызываем функцию для очистки текста
    clean_text(input_file, output_file)

# запускаем основную программу
if __name__ == '__main__':
    main()