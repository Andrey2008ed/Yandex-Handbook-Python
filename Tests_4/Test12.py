"""
Числовой прямоугольник
Ребята в детском саду учатся считать, и чтобы им было интереснее, воспитательница решила оформить список изучаемых 
чисел особым образом.
Дети справляются весьма быстро, поэтому ей требуется программа способная строить числовые прямоугольники. 
Напишите программу, которая строит числовой прямоугольник требуемого размера.

Формат ввода
В первой строке записано число N — высота числового прямоугольника.
Во второй строке указано число M — ширина числового прямоугольника.

Формат вывода
Нужно вывести сформированный числовой прямоугольник требуемого размера.
Чтобы прямоугольник был красивым, каждый его столбец должен быть одинаковой ширины.

Ввод:
2
3

Вывод:
1 2 3
4 5 6
"""

n = int(input())
m = int(input())
temp = 1
for i in range(n):
    for j in range(m):
        if n * m < 10:
            print("{:1}".format(temp), end=" ")
        elif n * m > 10 and n * m < 100:
            print("{:2}".format(temp), end=" ")
        else:
            print("{:3}".format(temp), end=" ")
        temp += 1
    print()

